package module1.examples;

public class PrimitiveDataType {
    public static void main(String[] args) {
        // Integer types
        int myInt = 42; // Represents a 32-bit signed integer
        short myShort = 12345; // Represents a 16-bit signed integer
        long myLong = 1234567890L; // Represents a 64-bit signed integer (note the 'L' suffix)

        // Floating-point types
        float myFloat = 3.14f; // Represents a 32-bit floating-point number (note the 'f' suffix)
        double myDouble = 2.71828; // Represents a 64-bit floating-point number

        // Character type
        char myChar = 'A'; // Represents a single Unicode character

        // Boolean type
        boolean isJavaFun = true; // Represents a true/false value

        // Output values
        System.out.println("Integer: " + myInt);
        System.out.println("Short: " + myShort);
        System.out.println("Long: " + myLong);
        System.out.println("Float: " + myFloat);
        System.out.println("Double: " + myDouble);
        System.out.println("Character: " + myChar);
        System.out.println("Boolean: " + isJavaFun);
    }
}
