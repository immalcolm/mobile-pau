package com.pau.simplerecyclerview;

public class Person {
    private String name;
    private String detail;

    public Person(String name, String detail) {
        this.name = name;
        this.detail = detail;
    }

    // Getters and Setters
    public String getName() {
        return name;
    }

    public String getDetail() {
        return detail;
    }
}